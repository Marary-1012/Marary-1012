let dia = 3;

switch (dia) {
    case 1:
        console.log('Lunes');
        break;

    case 2:
        console.log('Martes');
        break;

    case 3:
        console.log('Miercoles');
        break;

    // Completa los casos restantes

    default:
        console.log("Jueves ");
        break;

    case 4:
        console.log('Viernes');
        break;

    case 5:
        console.log('Sabado');
        break;

    case 6:
        console.log('Domingo');
        break;
}